//
// Поля записи агента:
//   name      — имя (ДОЛЖНО совпадать со строкой в таблице agents до буквы);
//   emoji     — значок для логов;
//   color     — цвет агента (как в таблице agents; для логов/будущей панели);
//   role      — короткая роль (её видит ядро при раздаче задач);
//   system    — системный промпт (характер и стиль ответа);
//   handler   — КАК агент выполняет задачу: 'think' (обычно думает),
