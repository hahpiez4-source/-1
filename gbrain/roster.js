//   critic    — true: это Критик, он рецензирует чужие готовые результаты;
//   archivist — true: это Архивариус, он хранит общую память (ему шлют итоги);
//   organizer — true: агент берёт «органайзерские» задачи (Google: календарь/
//               события/напоминания/Диск). Такие задачи ядро отдаёт ему
//               детерминированно, остальные агенты их не трогают.
// ============================================================

// Слова, по которым задача считается «органайзерской» (Google: календарь /
// события / напоминания / расписание / Диск). По ним задачи детерминированно
// уходят агенту с флагом organizer (Секретарю), а не случайному. Слово «задача»
// НАМЕРЕННО не берём — оно совпадает с задачами доски роя.
const ORGANIZER_RE =
  /(календар|событие|событи[яйе]|встреч|напомн|напоминани|расписани|дедлайн|ежедневник|google\s*calendar|гугл\s*календар|гугл\s*диск|google\s*drive|на\s*диске|папк[уа]|гугл\s*док|google\s*doc|документ на)/i;

export function isOrganizerTask(text) {
  return ORGANIZER_RE.test(String(text || ''));
}

