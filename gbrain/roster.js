// Найти полную запись агента по имени (или DEFAULT_AGENT, если имени нет).
export function getAgent(name) {
  return ROSTER.find((a) => a.name === name) || DEFAULT_AGENT;
}

// Совместимость со старым кодом: вернуть «характер» агента (emoji/role/system).
// (раньше это жило в personas.js — теперь личность хранится прямо в реестре).
export function getPersona(name) {
  const a = getAgent(name);
  return { emoji: a.emoji, role: a.role, system: a.system };
}

// Имя агента с заданным флагом (например, archivist/critic/organizer) — или null.
// Удобно, когда нужно узнать «а кто у нас Архивариус?», не зашивая имя в код.
export function roleHolder(flag) {
  return ROSTER.find((a) => a[flag])?.name ?? null;
}

