-- ============================================================
-- Защита базы роя gbrain: включаем Row Level Security (RLS).
--
-- Зачем: без RLS любой, у кого есть ПУБЛИЧНЫЙ ключ проекта (anon),
-- может читать и писать в эти таблицы через PostgREST. Это дыра.
--
-- Что делаем: включаем "замок" (RLS) на всех 4 таблицах.
-- После этого роли anon / authenticated по умолчанию НЕ имеют доступа
-- (нет ни одной разрешающей политики = всё запрещено).
--
-- Почему агенты не сломаются: они ходят в базу через секретный ключ
-- service_role, а он ВСЕГДА обходит RLS. Поэтому рой продолжит работать.
--
-- Политики для anon пока НЕ добавляем намеренно: фронтенда/дашборда нет,
-- публично читать нечего. Когда появится дашборд — добавим ровно нужный
-- доступ (например, read-only на отдельные таблицы) отдельной миграцией.
-- ============================================================

alter table public.agents   enable row level security;
alter table public.tasks    enable row level security;
alter table public.messages enable row level security;
alter table public.memory   enable row level security;
