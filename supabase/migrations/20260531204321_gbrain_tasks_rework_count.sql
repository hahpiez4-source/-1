-- ============================================================
-- gbrain — счётчик доработок: tasks.rework_count.
--
-- Когда Критик выносит вердикт «доработать», задача возвращается на доску.
-- Этот счётчик считает, сколько раз её уже возвращали, чтобы рой не
-- зациклился: после лимита Критик принимает результат как есть.
-- ============================================================

alter table public.tasks
  add column if not exists rework_count int not null default 0;

comment on column public.tasks.rework_count is
  'Сколько раз задачу возвращали на доработку (защита от бесконечного цикла ревью)';
